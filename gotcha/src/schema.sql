CREATE TABLE USERS (
	USERNAME    VARCHAR(10) NOT NULL PRIMARY KEY,
	PASSWORD    VARCHAR(8) NOT NULL,
	NICKNAME    VARCHAR(20) UNIQUE,
	DESCRIPTION VARCHAR(50),
	PHOTO       VARCHAR(150)
);

CREATE TABLE CHANNELS (
	NAME 		VARCHAR(30) PRIMARY KEY,
	DESCRIPTION VARCHAR(500) NOT NULL,
	CREATEDBY 	VARCHAR(10) NOT NULL,
	CREATED 	TIMESTAMP NOT NULL,
	SUBSCRIBERS INTEGER
);

CREATE TABLE SUBSCRIPTIONS (
	ID 			INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	USER 		VARCHAR(20) NOT NULL REFERENCES USERS(NICKNAME) ON DELETE CASCADE,
	CHANNEL 	VARCHAR(30) NOT NULL REFERENCES CHANNELS(NAME) ON DELETE CASCADE
);

CREATE TABLE DIRECT_MESSAGES (
	ID 			INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	TEXT 		VARCHAR(500) NOT NULL,
	SENDER 		VARCHAR(20) NOT NULL REFERENCES USERS(NICKNAME) ON DELETE CASCADE,
	RECEIVER 	VARCHAR(20) DEFAULT NULL REFERENCES USERS(NICKNAME) ON DELETE CASCADE,
	SENTTIME 	TIMESTAMP NOT NULL,
	READTIME	TIMESTAMP DEFAULT NULL
);

CREATE TABLE CHANNEL_MESSAGES (
	ID 			INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	TEXT 		VARCHAR(500) NOT NULL,
	SENDER 		VARCHAR(10) NOT NULL REFERENCES USERS(NAME) ON DELETE CASCADE,
	CHANNEL 	VARCHAR(30) DEFAULT NULL REFERENCES CHANNELS(NAME) ON DELETE CASCADE,
	REPLYFOR 	INTEGER REFERENCES CHANNEL_MESSAGES(ID) ON DELETE CASCADE,
	SENTTIME 	TIMESTAMP NOT NULL,
	READTIME	TIMESTAMP DEFAULT NULL
);